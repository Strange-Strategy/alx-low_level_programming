#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/** 
 * main - Entry point
 * programto print the last digit of a number
 * Return: 0 Always (Success)
 */
int main(void)
{
	int n;

	srand(time(0));
	n = rand() - RAND_MAX / 2;

	if (-1[n] > 5)
	{
		printf("Last digit of %d", n);
	}
	return (0);
}
